import turtle as t
t.shape('turtle')
t.bgcolor('purple')

t.color('white','red')
t.begin_fill()
t.color('white','red')
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)

t.penup()
t.forword(150)
t.pendown()

t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.end_fill()
t.mainloop()
